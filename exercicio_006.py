resultado = int(input('digite um numero'))
dobro = resultado * 2
triplo = resultado * 3
raiz = resultado ** 0.5
print ('O dobro de {} é igual a {}'. format(resultado, dobro))
print('O triplo de {} é igual a {}'. format(resultado, triplo))
print('A raiz quadrada de {}, é igual a {}'.format(resultado, raiz))